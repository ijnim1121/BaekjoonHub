# 입력 받기
month, day = map(int, input().split())

# 각 달의 일수
days_in_month = [0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31]

# 1월 1일부터 해당 날짜까지의 총 일수 계산
total_days = sum(days_in_month[:month]) + day

# 1월 1일의 기준: 문제에서 1월 1일은 월요일로 지정되어 있다. 따라서, 1월 1일을 0번째 날로 간주.
# 일수 계산: total_days는 1월 1일부터 입력한 날짜까지의 총 일수. 예를 들어, 1월 1일이 1일이라면, 1월 2일은 2일이 되고, 1월 3일은 3일.
# 0 기반 인덱스: 요일 계산을 할 때 0부터 시작하는 인덱스를 사용하기 위해서 1을 빼는 것. 즉, 1월 1일(월요일)은 0으로 계산하고, 1월 2일(화요일)은 1로 계산하여 요일에 맞게 매핑.

# 1월 1일이 월요일이므로 0으로 시작
# 0: 월요일, 1: 화요일, 2: 수요일, 3: 목요일, 4: 금요일, 5: 토요일, 6: 일요일
weekday = (total_days - 1) % 7

# 요일 리스트
weekdays = ["MON", "TUE", "WED", "THU", "FRI", "SAT", "SUN"]

# 결과 출력
print(weekdays[weekday])
